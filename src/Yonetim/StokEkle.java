    public void StokEkleAcilis() {
        jTextField1.setEnabled(false);
        jTextField4.setEnabled(false);
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jTextField3.setText(dtf.format(localDate));
        List<sqlsorgular.urunler> urunAdlari = null;
        try {
            urunAdlari = new sqlsorgular.urunler().urunadi();
        } catch (SQLException ex) {
            Logger.getLogger(StokEkle.class.getName()).log(Level.SEVERE, null, ex);
        }
        DefaultListModel listmodel = new DefaultListModel();
        for (int i = 0; i < urunAdlari.size(); i++) {
            System.out.println(urunAdlari.get(i).getUrunAdi());
            listmodel.add(i, urunAdlari.get(i).getUrun_adi());
        }
        jList1.setModel(listmodel);
        jList1.validate();
        jList1.repaint();

    }

