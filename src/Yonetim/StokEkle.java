    private void jList1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jList1MouseClicked
        // TODO add your handling code here:

        jTextField3.setText(dtf.format(localDate));
        int urunKodu = 0;
        try {
            urunKodu = new sqlsorgular.urunler().UrunKoduByAd(jList1.getSelectedValue());
        } catch (SQLException ex) {
            Logger.getLogger(StokEkle.class.getName()).log(Level.SEVERE, null, ex);
        }
        Urun_Kodu = urunKodu;
        jTextField1.setText(jList1.getSelectedValue());
        try {
            GuncelAdet = new sqlsorgular.stoklar().StokAdetbyUrunNo(urunKodu);
        } catch (SQLException ex) {
            Logger.getLogger(StokEkle.class.getName()).log(Level.SEVERE, null, ex);
        }

        jTextField4.setText(String.valueOf(GuncelAdet) + "  Adet");


    }//GEN-LAST:event_jList1MouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        boolean sonuc = false;
        int adet = 0;
        try {
            adet = Integer.parseInt(jTextField2.getText().trim());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "HATA", "Giriş tipi hatalı. Adet olarak yalnızca sayı giriniz", JOptionPane.ERROR_MESSAGE);
        }
        adet = adet + GuncelAdet;
        try {
            sonuc = new sqlsorgular.stoklar().StokGuncelle(Urun_Kodu, adet);
        } catch (SQLException ex) {
            Logger.getLogger(StokEkle.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (sonuc) {
            sonuc = new sqlsorgular.stoklar().StokLogKaydet(Urun_Kodu, Integer.parseInt(jTextField2.getText().trim()), "ADD");
            if (sonuc) {
                StokEkleAcilis();
            } else {
                JOptionPane.showMessageDialog(this, "HATA", "Kayıt Gerçekleştirilemedi", JOptionPane.ERROR_MESSAGE);

            }

        } else {
            JOptionPane.showMessageDialog(this, "HATA", "Kayıt Gerçekleştirilemedi", JOptionPane.ERROR_MESSAGE);

        }

    }//GEN-LAST:event_jButton1ActionPerformed

