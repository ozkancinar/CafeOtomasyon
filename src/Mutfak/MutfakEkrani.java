    public void SiparisAl(){
       
        List<sqlsorgular.siparis> siparisgetirildi = new ArrayList<>();
            try{
                siparisgetirildi = new mutfakthread().siparisdene();
            }catch(Exception e){
                Logger.getLogger(MutfakEkrani.class.getName()).log(Level.SEVERE, null, e);
            }
            DefaultTableModel tablo =(DefaultTableModel)jTable1.getModel();
            tablo.setRowCount(0);
            
            Vector[] v = new Vector[siparisgetirildi.size()];
            for(int n=0;n<siparisgetirildi.size();n++){
                v[n] = new Vector();
                v[n].add(siparisgetirildi.get(n).getSiparis_no());
                v[n].add(siparisgetirildi.get(n).getMasa_no());
                v[n].add(siparisgetirildi.get(n).getUrun_adi());
                v[n].add(siparisgetirildi.get(n).getAdet());
                v[n].add(siparisgetirildi.get(n).getNot());
                //tablo.setValueAt(v[n], n-1, 0);
                tablo.insertRow(0,v[n]);
                //tablo.fireTableRowsInserted(v[n].size()-1, v[n].size()-1);
                    //System.out.println("vektor");
            }
            jTable1.setModel(tablo);
            jTable1.repaint();
            jTable1.revalidate();
            jTable1.getUpdateSelectionOnSort();
            this.repaint();
            this.revalidate();
            this.validate();
    }
