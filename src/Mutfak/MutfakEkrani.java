    private void onaylaButonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_onaylaButonActionPerformed
        sqlsorgular.siparis s=new siparis();
        int row =jTable1.getSelectedRow();
        String siparis_no =(String)jTable1.getModel().getValueAt(row, 0); 
        String masa_no = (String)jTable1.getModel().getValueAt(row, 1);
        String urunadi= (String)jTable1.getModel().getValueAt(row, 2);
        int adet = (int) jTable1.getModel().getValueAt(row, 3);
        String ozellik = (String)jTable1.getModel().getValueAt(row, 4);
        System.out.println(urunadi);
        int urun_kod=0;
        try {
            urun_kod= s.urunkoduGetir(urunadi);
            
        } catch (SQLException ex) {
            Logger.getLogger(MutfakEkrani.class.getName()).log(Level.SEVERE, null, ex);
            
        }
        int idsiparis=0;
        try {
            idsiparis = s.siparisid_Bul(siparis_no, masa_no, urun_kod, adet);
        } catch (SQLException ex) {
            Logger.getLogger(MutfakEkrani.class.getName()).log(Level.SEVERE, null, ex);
             
        }
        boolean durum=false;
        try{
            durum = s.siparisSil(idsiparis);
            if(durum){
                DefaultTableModel tablo =(DefaultTableModel)jTable1.getModel();
                (tablo).removeRow(jTable1.convertRowIndexToModel(row));
                new stoklar().StokAzalt(urun_kod, adet);
                new stoklar().StokLogKaydet(urun_kod, adet, "REMOVE");
                
            }else{
                 JOptionPane.showMessageDialog(this, "Silinirken hata oldu");
             }
        }catch(SQLException ex){
            Logger.getLogger(MutfakEkrani.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_onaylaButonActionPerformed

